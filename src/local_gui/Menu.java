    private void buttonAction1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAction1ActionPerformed
        if(menu.getSelectedtitulo().equals("Cartola"))
        {
            Cartola ca = new Cartola();
            ca.setVisible(true);
            this.setVisible(false);
            
        }
        if(menu.getSelectedtitulo().equals("Cliente"))
        {
            Cliente cli = new Cliente();
            cli.setVisible(true);
            this.setVisible(false);
        }
        if(menu.getSelectedtitulo().equals("Traspaso"))
        {
            Traspaso tr = new Traspaso();
            tr.setVisible(true);
            this.setVisible(false);
        }
        if(menu.getSelectedtitulo().equals("Giro"))
        {
            Giro gr = new Giro();
            gr.setVisible(true);
            this.setVisible(false);
        }
        if(menu.getSelectedtitulo().equals("Deposito"))
        {
            Deposito dp = new Deposito();
            dp.setVisible(true);
            this.setVisible(false);
        }
        if(menu.getSelectedtitulo().equals("Salir"))
        {
            System.exit(0);
        }
    }//GEN-LAST:event_buttonAction1ActionPerformed

