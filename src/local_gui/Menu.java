    public void llenarMenu()
    {
    List<Avatar> avatars=new ArrayList<Avatar>();
    avatars.add(new Avatar("Cartola",loadImage("/img/Cartola.png")));
    avatars.add(new Avatar("Cliente",loadImage("/img/Cliente.png")));
    avatars.add(new Avatar("Traspaso",loadImage("/img/Traspaso.png")));
    avatars.add(new Avatar("Deposito",loadImage("/img/Deposito.png")));
    avatars.add(new Avatar("Giro",loadImage("/img/Giro.png")));
    avatars.add(new Avatar("Salir",loadImage("/img/Salir.png")));
    menu.setAvatars(avatars);
    }
    
    public static Image loadImage(String fileName)
    {
        try {
            return ImageIO.read(Menu.class.getResource(fileName));
        } catch (Exception e)  {
        return null;
        }
    }
